MarchingCubes::MarchingCubes(float*** chunkArray, TArray<int>& indices, TArray<FVector>& vertices, TArray<FVector>& normals, const int chunkSize, const float isolevel, const float voxelSize)
{
	ConstructMesh(chunkArray, indices, vertices, normals, chunkSize, isolevel,voxelSize);
}

MarchingCubes::~MarchingCubes()
{
}

void MarchingCubes::ConstructMesh(float*** chunkArray, TArray<int>& indices, TArray<FVector>& vertices, TArray<FVector>& normals, const int chunkSize, const float isolevel, const float voxelSize)
{
	int numberOfIndices{ 0 };

	for (int x{ 0 }; x < chunkSize; x++)
	{
		for (int y{ 0 }; y < chunkSize; y++)
		{
			for (int z{0}; z < chunkSize; z++)
			{
				//variables
				FVector corners[8];
				FVector vertexList[12];
				FVector newVertexList[12];
				int localRemap[12];
				float densities[8];
				int cubeIndex = 0;
				TArray<FVector> tempNormals;

				//calculate position of voxel
				FVector voxelPos{0.0f,0.0f,0.0f};
				voxelPos.X += (x * voxelSize);
				voxelPos.Y += (y * voxelSize);
				voxelPos.Z += (z * voxelSize);

				//Get densities
				for (int i{ 0 }; i < 8; ++i)
				{
					densities[i] = GetDensity(i, x, y, z, chunkArray);
				}

				//calculate voxel corner positions
				corners[0] = FVector(voxelPos.X, voxelPos.Y, voxelPos.Z);
				corners[1] = FVector(voxelPos.X + voxelSize, voxelPos.Y, voxelPos.Z);
				corners[2] = FVector(voxelPos.X + voxelSize, voxelPos.Y, voxelPos.Z + voxelSize);
				corners[3] = FVector(voxelPos.X, voxelPos.Y, voxelPos.Z + voxelSize);
				corners[4] = FVector(voxelPos.X, voxelPos.Y + voxelSize, voxelPos.Z);
				corners[5] = FVector(voxelPos.X + voxelSize, voxelPos.Y + voxelSize, voxelPos.Z);
				corners[6] = FVector(voxelPos.X + voxelSize, voxelPos.Y + voxelSize, voxelPos.Z + voxelSize);
				corners[7] = FVector(voxelPos.X, voxelPos.Y + voxelSize, voxelPos.Z + voxelSize);

				//construct cubeindex
				if (densities[0] > isolevel) cubeIndex |= 1;
				if (densities[1] > isolevel) cubeIndex |= 2;
				if (densities[2] > isolevel) cubeIndex |= 4;
				if (densities[3] > isolevel) cubeIndex |= 8;
				if (densities[4] > isolevel) cubeIndex |= 16;
				if (densities[5] > isolevel) cubeIndex |= 32;
				if (densities[6] > isolevel) cubeIndex |= 64;
				if (densities[7] > isolevel) cubeIndex |= 128;

				
				// no triangles if it is surrounded by air or surrounded by blocks
				if (Table::m_Edges[cubeIndex] == 0 || Table::m_Edges[cubeIndex] == 255) continue;

				// Find the vertices where the surface intersects the cube
				if ((Table::m_Edges[cubeIndex] & 1) == 1)
					vertexList[0] = VertexInterp(corners[0], corners[1], densities[0], densities[1], isolevel);
				if ((Table::m_Edges[cubeIndex] & 2) == 2)
					vertexList[1] = VertexInterp(corners[1], corners[2], densities[1], densities[2], isolevel);
				if ((Table::m_Edges[cubeIndex] & 4) == 4)
					vertexList[2] = VertexInterp(corners[2], corners[3], densities[2], densities[3], isolevel);
				if ((Table::m_Edges[cubeIndex] & 8) == 8)
					vertexList[3] = VertexInterp(corners[3], corners[0], densities[3], densities[0], isolevel);
				if ((Table::m_Edges[cubeIndex] & 16) == 16)
					vertexList[4] = VertexInterp(corners[4], corners[5], densities[4], densities[5], isolevel);
				if ((Table::m_Edges[cubeIndex] & 32) == 32)
					vertexList[5] = VertexInterp(corners[5], corners[6], densities[5], densities[6], isolevel);
				if ((Table::m_Edges[cubeIndex] & 64) == 64)
					vertexList[6] = VertexInterp(corners[6], corners[7], densities[6], densities[7], isolevel);
				if ((Table::m_Edges[cubeIndex] & 128) == 128)
					vertexList[7] = VertexInterp(corners[7], corners[4], densities[7], densities[4], isolevel);
				if ((Table::m_Edges[cubeIndex] & 256) == 256)
					vertexList[8] = VertexInterp(corners[0], corners[4], densities[0], densities[4], isolevel);
				if ((Table::m_Edges[cubeIndex] & 512) == 512)
					vertexList[9] = VertexInterp(corners[1], corners[5], densities[1], densities[5], isolevel);
				if ((Table::m_Edges[cubeIndex] & 1024) == 1024)
					vertexList[10] = VertexInterp(corners[2], corners[6], densities[2], densities[6], isolevel);
				if ((Table::m_Edges[cubeIndex] & 2048) == 2048)
					vertexList[11] = VertexInterp(corners[3], corners[7], densities[3], densities[7], isolevel);


				int newVertexCount = 0;
				for (int i{ 0 }; i < 12; i++)
					localRemap[i] = -1;

				for (UINT i = 0; Table::m_Triangles[cubeIndex][i] != -1; i++)
				{
					if (localRemap[Table::m_Triangles[cubeIndex][i]] == -1)
					{
						newVertexList[newVertexCount] = vertexList[Table::m_Triangles[cubeIndex][i]];
						localRemap[Table::m_Triangles[cubeIndex][i]] = newVertexCount;
						newVertexCount++;
					}
				}

				int newVertices{0};
				
				for (UINT i = 0; Table::m_Triangles[cubeIndex][i] != -1; i += 3)
				{
					int indiceIndex1 = localRemap[Table::m_Triangles[cubeIndex][i + 2]];
					int indiceIndex2 = localRemap[Table::m_Triangles[cubeIndex][i + 1]];
					int indiceIndex3 = localRemap[Table::m_Triangles[cubeIndex][i]];

					//add vertices
					vertices.Add(newVertexList[indiceIndex1]);
					tempNormals.Add(FVector(0.0f, 0.0f, 0.0f));
					vertices.Add(newVertexList[indiceIndex2]);
					tempNormals.Add(FVector(0.0f, 0.0f, 0.0f));
					vertices.Add(newVertexList[indiceIndex3]);
					tempNormals.Add(FVector(0.0f, 0.0f, 0.0f));

					//add triangles
					indices.Add(numberOfIndices);
					indices.Add(numberOfIndices + 1);
					indices.Add(numberOfIndices + 2);

					//normals
					FVector normal{ 0.0f,0.0f,0.0f };
					FVector p1 = newVertexList[indiceIndex1];
					FVector p2 = newVertexList[indiceIndex2];
					FVector p3 = newVertexList[indiceIndex3];
					normal += FVector::CrossProduct((p2 - p1), (p3 - p1));
					normal.Normalize();
					tempNormals[i] += normal;
					tempNormals[i + 1] += normal;
					tempNormals[i + 2] += normal;

				
					numberOfIndices += 3;
					newVertices += 3;
				}

				//normals
				for (int i{ 0 }; i < newVertices; i++)
				{
					tempNormals[i].Normalize();
					tempNormals[i] *= -1.0f;
					//normals that point straight down cause triangles to look pitch black so slightly move them a bit
					if (tempNormals[i].Z < -0.75f)
					{
						tempNormals[i] += FVector(0.5f, 0.5f, 0.0f);
						tempNormals[i].Normalize();
					}
					normals.Add(tempNormals[i]);
				}
			}
		}
	}
}

FVector MarchingCubes::VertexInterp(const FVector &vertex1, const FVector &vertex2, const float val1, const float val2, const float isolevel)
{
	//Calculate vertex position on voxel edge
	if (fabs(isolevel - val1) < 0.00001)
		return(vertex1);
	if (fabs(isolevel - val2) < 0.00001)
		return(vertex2);
	if (fabs(val1 - val2) < 0.00001)
		return(vertex1);
	auto mu = (isolevel - val1) / (val2 - val1);
	FVector p;
	p.X = vertex1.X + mu * (vertex2.X - vertex1.X);
	p.Y = vertex1.Y + mu * (vertex2.Y - vertex1.Y);
	p.Z = vertex1.Z + mu * (vertex2.Z - vertex1.Z);

	return(p);
}

float MarchingCubes::GetDensity(const int cornerIndex, const int voxelIndexX, const int voxelIndexY, const int voxelIndexZ, float*** chunkArray)
{
	switch (cornerIndex)
	{
	case 0:

		return chunkArray[voxelIndexX][voxelIndexY][voxelIndexZ];
			break;
	case 1:

		return chunkArray[voxelIndexX + 1][voxelIndexY][voxelIndexZ];
		break;
	case 2:

		return chunkArray[voxelIndexX + 1][voxelIndexY][voxelIndexZ + 1];
		break;
	case 3:
	
		return chunkArray[voxelIndexX][voxelIndexY][voxelIndexZ + 1];
		break;
	case 4:
		
		return chunkArray[voxelIndexX][voxelIndexY + 1][voxelIndexZ];
		break;
	case 5:
		
		return chunkArray[voxelIndexX + 1][voxelIndexY + 1][voxelIndexZ];
		break;
	case 6:
		
		return chunkArray[voxelIndexX + 1][voxelIndexY + 1][voxelIndexZ + 1];
		break;
	case 7:
		
		return chunkArray[voxelIndexX][voxelIndexY + 1][voxelIndexZ + 1];
		break;
	}
	return 0.0f;
}

